def search_curated(query: str = None, author: str = None, genre: str = None, language: str = None) -> list:
    """Search curated books with intelligent matching and scoring"""
    results = []
    q = (query or "").lower().strip()
    a = (author or "").lower().strip()
    g = (genre or "").lower().strip()
    l = (language or "").lower().strip()
    
    for b in CURATED_BOOKS:
        score = 0  # Relevance score
        match = True
        
        title_lower = b["title"].lower()
        authors_str = " ".join(b["authors"]).lower()
        categories_str = " ".join(b["categories"]).lower()
        blob = f"{title_lower} {authors_str} {categories_str}"
        
        # Query matching with scoring
        if q:
            # Exact title match (highest priority)
            if q == title_lower:
                score += 100
            # Title starts with query
            elif title_lower.startswith(q):
                score += 50
            # Query in title
            elif q in title_lower:
                score += 30
            # Query in blob
            elif q in blob:
                score += 10
            else:
                match = False
        
        # Author matching
        if a:
            if a in authors_str:
                score += 20
            else:
                match = False
        
        # Genre matching
        if g:
            if g in categories_str:
                score += 15
            else:
                match = False
        
        # Language matching
        if l and b["language"] != l:
            match = False
        
        if match:
            # Add rating as tiebreaker
            score += b.get("average_rating", 0) or 0
            results.append((score, b))
    
    # Sort by score descending, then return books only
    results.sort(reverse=True, key=lambda x: x[0])
    return [b for score, b in results]

def curated_items_list() -> list:
    return CURATED_BOOKS

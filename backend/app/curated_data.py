def search_curated(query: str = None, author: str = None, genre: str = None, language: str = None) -> list:
    results = []
    q = (query or "").lower()
    a = (author or "").lower()
    g = (genre or "").lower()
    l = (language or "").lower()
    
    for b in CURATED_BOOKS:
        match = True
        blob = (b["title"] + " " + " ".join(b["authors"]) + " " + " ".join(b["categories"])).lower()
        if q and q not in blob: match = False
        if a and a not in " ".join(b["authors"]).lower(): match = False
        if g and g not in " ".join(b["categories"]).lower(): match = False
        if l and b["language"] != l: match = False
        if match: results.append(b)
    return results

def curated_items_list() -> list:
    return CURATED_BOOKS
